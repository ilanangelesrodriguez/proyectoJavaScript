//Sentencias Switch

let nota=5;

switch (nota){
    case 1:
        console.log("Muy mal");
        break;
    case 2:
        console.log("Deberias esforzarte mas");
        break;
    case 3:
        console.log("Aprobaste, pero sigue intentado");
        break;
    case 4:
        console.log("Muy bien");
        break;
    case 5:
        console.log("Felicitaciones has obtenido la mayor calificacion");
        break;
    default:
        console.log("Error");
        break;
}
 
